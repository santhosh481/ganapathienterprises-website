/* ================= NEXT STEP ================= */

function nextStep(step) {

    /* STEP 1 VALIDATION */

    if (step === 2) {

        if (!selectedService) {

            showNotification(
                'Please select a service',
                'error'
            );

            return;
        }
    }

    /* STEP 4 SUMMARY */

    if (step === 4) {

        const bookingDate =
            document.getElementById('bookingDate');

        const bookingTime =
            document.getElementById('bookingTime');

        document.getElementById('summaryService').textContent =
            selectedService || '';

        document.getElementById('summaryName').textContent =
            document.getElementById('bookingName')?.value || '';

        document.getElementById('summaryMobile').textContent =
            document.getElementById('bookingMobile')?.value || '';

        document.getElementById('summaryEmail').textContent =
            document.getElementById('bookingEmail')?.value || '';

        document.getElementById('summaryCity').textContent =
            document.getElementById('bookingCity')?.value || '';

        document.getElementById('summaryArea').textContent =
            (document.getElementById('bookingArea')?.value || '') + ' sq.ft';

        document.getElementById('summaryDateTime').textContent =
            `${bookingDate?.value || ''} ${bookingTime?.value || ''}`;
    }

    /* HIDE ALL STEPS */

    document.getElementById('step1Form')
        .classList.add('hidden');

    document.getElementById('step2Form')
        .classList.add('hidden');

    document.getElementById('step3Form')
        .classList.add('hidden');

    document.getElementById('step4Form')
        .classList.add('hidden');

    /* SHOW CURRENT STEP */

    const currentForm =
        document.getElementById(`step${step}Form`);

    if (currentForm) {
        currentForm.classList.remove('hidden');
    }

    /* UPDATE STEP INDICATORS */

    for (let i = 1; i <= 4; i++) {

        const stepEl =
            document.getElementById(`step${i}`);

        if (!stepEl) continue;

        if (i < step) {

            stepEl.classList.add('completed');
            stepEl.classList.remove('active');

        } else if (i === step) {

            stepEl.classList.add('active');
            stepEl.classList.remove('completed');

        } else {

            stepEl.classList.remove(
                'active',
                'completed'
            );
        }
    }
}


/* ===== BOOKING STEP-WISE FORM FIX ===== */
let bookingCurrentStep = 1;
const bookingTotalSteps = 4;

function bookingValidateStep(step){
    const get = (id) => (document.getElementById(id)?.value || '').trim();
    if(step === 1){
        if(!get('bookingName') || !get('bookingMobile')){ alert('Please enter customer name and mobile number.'); return false; }
        if(get('bookingMobile').length !== 10){ alert('Please enter a valid 10 digit mobile number.'); return false; }
    }
    if(step === 2){
        if(!get('bookingProperty') || !get('bookingStatus')){ alert('Please select property type and property status.'); return false; }
    }
    if(step === 4){
        if(!get('bookingCity') || !get('bookingAddress')){ alert('Please enter Area / City and Full Address.'); return false; }
    }
    return true;
}

function bookingShowStep(step){
    bookingCurrentStep = Math.max(1, Math.min(bookingTotalSteps, step));
    document.querySelectorAll('[data-booking-step]').forEach(function(card){
        card.classList.toggle('active', Number(card.getAttribute('data-booking-step')) === bookingCurrentStep);
    });
    document.querySelectorAll('[data-booking-tab]').forEach(function(tab){
        const tabStep = Number(tab.getAttribute('data-booking-tab'));
        tab.classList.toggle('active', tabStep === bookingCurrentStep);
        tab.classList.toggle('completed', tabStep < bookingCurrentStep);
    });
    const prev = document.getElementById('bookingPrevBtn');
    const next = document.getElementById('bookingNextBtn');
    if(prev) prev.disabled = bookingCurrentStep === 1;
    if(next){
        next.style.display = bookingCurrentStep === bookingTotalSteps ? 'none' : 'inline-flex';
    }
    if(typeof updateBookingPreviewV2 === 'function') updateBookingPreviewV2();
}

function bookingNextStep(){
    if(!bookingValidateStep(bookingCurrentStep)) return;
    bookingShowStep(bookingCurrentStep + 1);
}

function bookingPrevStep(){
    bookingShowStep(bookingCurrentStep - 1);
}

function getCheckedBookingServices(){
    return Array.from(document.querySelectorAll('.booking-service-check:checked')).map(function(item){ return item.value; }).join(', ');
}

document.addEventListener('DOMContentLoaded', function(){
    document.querySelectorAll('[data-booking-tab]').forEach(function(tab){
        tab.addEventListener('click', function(){
            const targetStep = Number(tab.getAttribute('data-booking-tab'));
            if(targetStep <= bookingCurrentStep || bookingValidateStep(bookingCurrentStep)) bookingShowStep(targetStep);
        });
    });
    bookingShowStep(1);
});

